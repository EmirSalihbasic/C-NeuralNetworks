#include <iostream>

int main() {
    // Inputs
    bool input1 = true;
    bool input2 = false;

    // Logical AND gate
    bool result = input1 && input2;

    // Print the result
    std::cout << "Input 1: " << input1 << std::endl;
    std::cout << "Input 2: " << input2 << std::endl;
    std::cout << "AND Gate Result: " << result << std::endl;

    return 0;
}